export const getQuestById = (id: string): Quest | undefined => {
  return ALL_QUESTS.find(q => q.id === id);
};

export const getQuestsByCategory = (category: string): Quest[] => {
  return ALL_QUESTS.filter(q => q.category === category);
};

export const getDailyQuestIds = (): string[] => {
  return DAILY_QUESTS.map(q => q.id);
};

export const getChainById = (chainId: string): QuestChain | undefined => {
  return QUEST_CHAINS.find(c => c.id === chainId);
};

export const getChainForQuest = (questId: string): QuestChain | undefined => {
  return QUEST_CHAINS.find(c => c.questIds.includes(questId));
};

