export const WORKSHOP_LEVEL_EXP = [
  0, 100, 300, 600, 1000, 1500, 2100, 2800, 3600, 4500,
  5500, 6600, 7800, 9100, 10500,
];

export const getWorkshopLevelExp = (level: number): number => {
  if (level <= 0) return 0;
  if (level >= WORKSHOP_LEVEL_EXP.length) return WORKSHOP_LEVEL_EXP[WORKSHOP_LEVEL_EXP.length - 1];
  return WORKSHOP_LEVEL_EXP[level];
};

export const getRepairStageInfo = (stage: RepairStage) => {
  return REPAIR_STAGES.find(s => s.key === stage) || REPAIR_STAGES[0];
};
