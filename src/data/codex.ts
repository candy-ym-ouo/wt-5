export const getAuthorById = (id: string): Author | undefined => {
  return AUTHORS.find(a => a.id === id);
};

export const getAuthorByBookId = (bookId: string): Author | undefined => {
  return AUTHORS.find(a => a.bookIds.includes(bookId));
};

export const getThemeCollectionById = (id: string): ThemeCollection | undefined => {
  return THEME_COLLECTIONS.find(t => t.id === id);
};

export const getEasterEggById = (id: string): EasterEgg | undefined => {
  return EASTER_EGGS.find(e => e.id === id);
};

export const getEasterEggsByBookId = (bookId: string): EasterEgg[] => {
  return EASTER_EGGS.filter(e => {
    const cond = e.unlockCondition;
    if (cond.type === 'find_book') return cond.bookId === bookId;
    if (cond.type === 'find_all_books') return cond.bookIds?.includes(bookId);
    return false;
  });
};

