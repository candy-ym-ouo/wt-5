export function getCharacterById(id: string): GameCharacter | undefined {
  return GAME_CHARACTERS.find(c => c.id === id);
}

export function getDialogueTreesForCharacter(characterId: string): CharacterDialogueTree[] {
  return CHARACTER_DIALOGUE_TREES.filter(d => d.characterId === characterId);
}

export function getDialogueTreeById(id: string): CharacterDialogueTree | undefined {
  return CHARACTER_DIALOGUE_TREES.find(d => d.id === id);
}

export function getBooklistsForCharacter(characterId: string): ExclusiveBookList[] {
  return EXCLUSIVE_BOOKLISTS.filter(b => b.characterId === characterId);
}

export function getSideQuestsForCharacter(characterId: string): CharacterSideQuest[] {
  return CHARACTER_SIDE_QUESTS.filter(q => q.characterId === characterId);
}

export function getAchievementsForCharacter(characterId: string): CharacterAchievement[] {
  return CHARACTER_ACHIEVEMENTS.filter(a => a.characterId === characterId);
}

export function getAvailableDialogueTree(characterId: string, completedTreeIds: string[], relationshipLevel: RelationshipLevel): CharacterDialogueTree | undefined {
  const trees = CHARACTER_DIALOGUE_TREES.filter(d => {
    if (d.characterId !== characterId) return false;
    if (!d.repeatable && completedTreeIds.includes(d.id)) return false;
    const reqIdx = ['stranger', 'acquaintance', 'familiar', 'trusted', 'confidant'].indexOf(d.relationshipRequired);
    const curIdx = ['stranger', 'acquaintance', 'familiar', 'trusted', 'confidant'].indexOf(relationshipLevel);
    if (curIdx < reqIdx) return false;
    return true;
  });
  trees.sort((a, b) => b.priority - a.priority);
  return trees.length > 0 ? trees[0] : undefined;
}
